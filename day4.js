const gridRows = grid[0].length;
const gridColumns = grid.length;

let rollPosition = [];
let totalRollsTaken = 0;
let shouldCheckAgain = true;

function checkingAccesibility(row, column) {
  let count = 0;

  if (grid[row][column] !== "@") {
    return;
  }

  // top left
  if (row - 1 >= 0 && column - 1 >= 0 && grid[row - 1][column - 1] === "@") {
    count++;
  }

  // top
  if (row - 1 >= 0 && grid[row - 1][column] === "@") {
    count++;
  }

  // top right
  if (
    row - 1 >= 0 &&
    column + 1 < gridColumns &&
    grid[row - 1][column + 1] === "@"
  ) {
    count++;
  }

  // left
  if (column - 1 >= 0 && grid[row][column - 1] === "@") {
    count++;
  }

  // right
  if (column + 1 < gridColumns && grid[row][column + 1] === "@") {
    count++;
  }

  // bottom left
  if (
    row + 1 < gridRows &&
    column - 1 >= 0 &&
    grid[row + 1][column - 1] === "@"
  ) {
    count++;
  }

  // bottom
  if (row + 1 < gridRows && grid[row + 1][column] === "@") {
    count++;
  }

  // bottom right
  if (
    row + 1 < gridRows &&
    column + 1 < gridColumns &&
    grid[row + 1][column + 1] === "@"
  ) {
    count++;
  }

  if (count < 4) {
    totalRollsTaken++;
    rollPosition.push([row, column]);
    shouldCheckAgain = true;
  }
}

for (let i = 0; i < gridRows; i++) {
  for (let j = 0; j < gridColumns; j++) {
    checkingAccesibility(i, j);
  }
}

function takingRolls(position) {
  for (let i = 0; i < rollPosition.length; i++) {
    grid[position[i][0]] =
      grid[position[i][0]].slice(0, position[i][1]) +
      "x" +
      grid[position[i][0]].slice(position[i][1] + 1);
  }
}

takingRolls(rollPosition);

while (shouldCheckAgain) {
  shouldCheckAgain = false;
  for (let i = 0; i < gridRows; i++) {
    for (let j = 0; j < gridColumns; j++) {
      checkingAccesibility(i, j);
    }
  }
  takingRolls(rollPosition);
  rollPosition = [];
}

console.log(`totalRollsTaken ${totalRollsTaken}`);
